int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
		return (0);
	while (i <= nb / i)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	if (nb <= 2)
		return (2);
	while (nb > 2)
	{
		if (ft_is_prime(nb) == 1)
			return (nb);
		nb++;
	}
	return (0);
}
#include <stdio.h>
int main()
{
	printf("%d\n", ft_find_next_prime(2147483643));
	printf("%d\n", ft_find_next_prime(-2147483648));
	printf("%d", ft_find_next_prime(21));
}
